POLISH_ROOT="$ROOT/polish-gate"

pass_count=0

fail() {
  printf 'FAIL %s\n' "$1" >&2
  exit 1
}

pass() {
  printf 'PASS %s\n' "$1"
  pass_count=$((pass_count + 1))
}

need() {
  command -v "$1" >/dev/null 2>&1 || fail "missing command: $1"
}

find_bash4() {
  local candidate version
  for candidate in /opt/homebrew/bin/bash /usr/local/bin/bash bash; do
    command -v "$candidate" >/dev/null 2>&1 || continue
    version="$("$candidate" -c 'printf "%s\n" "${BASH_VERSINFO[0]}"' 2>/dev/null || true)"
    [[ "$version" -ge 4 ]] 2>/dev/null && { printf '%s\n' "$candidate"; return 0; }
  done
  return 1
}

assert_file() {
  [[ -f "$1" ]] || fail "missing file: $1"
}

assert_executable() {
  [[ -x "$1" ]] || fail "not executable: $1"
}

assert_grep() {
  local pattern="$1" file="$2" label="$3"
  grep -q -- "$pattern" "$file" || fail "$label"
}

assert_jq() {
  local file="$1" filter="$2" label="$3"
  jq -e "$filter" "$file" >/dev/null || {
    jq . "$file" >&2 || true
    fail "$label"
  }
}

validate_json_schema() {
  python3 -c '
import json
import sys
from jsonschema import Draft202012Validator

schema_path, instance_path = sys.argv[1], sys.argv[2]
with open(schema_path, encoding="utf-8") as handle:
    schema = json.load(handle)
with open(instance_path, encoding="utf-8") as handle:
    instance = json.load(handle)
Draft202012Validator.check_schema(schema)
Draft202012Validator(schema, format_checker=Draft202012Validator.FORMAT_CHECKER).validate(instance)
' "$1" "$2" || fail "$3"
}

expect_json_schema_invalid() {
  python3 -c '
import json
import sys
import jsonschema
from jsonschema import Draft202012Validator

schema_path, instance_path = sys.argv[1], sys.argv[2]
with open(schema_path, encoding="utf-8") as handle:
    schema = json.load(handle)
with open(instance_path, encoding="utf-8") as handle:
    instance = json.load(handle)
try:
    Draft202012Validator(schema).validate(instance)
except jsonschema.ValidationError:
    raise SystemExit(0)
raise SystemExit("expected invalid fixture to fail schema validation")
' "$1" "$2" || fail "$3"
}

