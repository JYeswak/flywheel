render_template() {
  "$BASH4" "$RENDER" "$@"
}

render_install_fixture() {
  local repo="$1" subs="$2" template out
  mkdir -p "$repo/.flywheel"
  write_substitutions "$subs" "$repo"
  for template in MISSION.md.tmpl GOAL.md.tmpl STATE.md.tmpl loop.json.tmpl; do
    out="$repo/.flywheel/${template%.tmpl}"
    [[ "$template" == "loop.json.tmpl" ]] && out="$repo/.flywheel/loop.json"
    render_template "$ROOT/$template" <"$subs" >"$out"
    ! grep -q '{{[^}][^}]*}}' "$out" || fail "unsubstituted marker in $out"
  done
  cp -R "$POLISH_ROOT" "$repo/.flywheel/"
  find "$repo/.flywheel/polish-gate" -type d -name __pycache__ -prune -exec rm -rf {} +
  cp "$repo/.flywheel/polish-gate/fixtures/bootstrap-mode.json" "$repo/.flywheel/polish-gate/manifest.json"
  chmod +x "$repo/.flywheel/polish-gate/discover-surfaces.py" "$repo/.flywheel/polish-gate/discover-surfaces.sh"
}

