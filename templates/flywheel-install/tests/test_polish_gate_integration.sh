validate_scope_allowlist_consumption() {
  python3 -c '
import fnmatch
import json
import sys
from pathlib import Path

import jsonschema
from jsonschema import Draft202012Validator

scope_schema_path, fixture_dir_arg, discovery_path, template_schema_path = sys.argv[1:5]
fixture_dir = Path(fixture_dir_arg)
with open(scope_schema_path, encoding="utf-8") as handle:
    scope_schema = json.load(handle)
validator = Draft202012Validator(scope_schema)

with open(template_schema_path, encoding="utf-8") as handle:
    template_schema = json.load(handle)
declared = template_schema["properties"]["polish_gate"]["properties"]["scope_allowlist_profile"]["properties"]["profiles"]["const"]
actual = []
profiles = {}
for path in sorted(fixture_dir.glob("*.json")):
    if path.name == "malformed-no-allowlist.json":
        continue
    with path.open(encoding="utf-8") as handle:
        payload = json.load(handle)
    validator.validate(payload)
    profiles[payload["profile_name"]] = payload
    actual.append(payload["profile_name"])
    if payload["profile_name"] != path.stem:
        raise AssertionError(f"profile_name mismatch for {path}")
if set(actual) != set(declared):
    raise AssertionError(f"declared profiles {declared} != fixtures {actual}")

with (fixture_dir / "malformed-no-allowlist.json").open(encoding="utf-8") as handle:
    malformed = json.load(handle)
try:
    validator.validate(malformed)
except jsonschema.ValidationError:
    pass
else:
    raise AssertionError("malformed-no-allowlist.json validated")

def matches(pattern, rel):
    if pattern == "**/*":
        return True
    if pattern.endswith("/"):
        return rel == pattern[:-1] or rel.startswith(pattern)
    if pattern.endswith("/**"):
        prefix = pattern[:-3]
        return rel == prefix or rel.startswith(prefix + "/")
    return fnmatch.fnmatchcase(rel, pattern)

def allowed(profile, rel):
    if any(matches(pattern, rel) for pattern in profile["blocklist_paths"]):
        return False
    return any(matches(pattern, rel) for pattern in profile["allowlist_paths"])

with open(discovery_path, encoding="utf-8") as handle:
    discovery = json.load(handle)
default_profile = profiles["default"]
for surface in discovery["surfaces"]:
    rel = surface["path"]
    if not allowed(default_profile, rel):
        raise AssertionError(f"default profile did not allow discovered surface {rel}")
excluded = {row["path"] for row in discovery["scope_excluded"]}
for rel in ["src/insurance-policies/policy_model.py", "scripts/fetch_run_log.py", "docs/domain/product.md"]:
    if rel not in excluded:
        raise AssertionError(f"domain fixture was not scope-excluded: {rel}")
    if allowed(default_profile, rel):
        raise AssertionError(f"default profile allowed excluded domain path {rel}")
' "$1" "$2" "$3" "$4" || fail "scope allowlist fixtures were not consumed against discovery output"
}

