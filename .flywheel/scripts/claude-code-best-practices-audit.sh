#!/usr/bin/env bash
set -euo pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd -P)"
SKILLS_ROOT="${HOME}/.claude/skills"
HOOKS_ROOT="${HOME}/.claude/hooks"
SETTINGS_JSON="${HOME}/.claude/settings.json"
DATE="2026-05-19"
WRITE_REPORTS=0
JSON_OUT=0

usage() {
  cat <<'EOF'
Usage: claude-code-best-practices-audit.sh [--write-reports] [--json]
       [--repo PATH] [--skills-root PATH] [--hooks-root PATH] [--settings PATH]
EOF
}

while [[ $# -gt 0 ]]; do
  case "$1" in
    --write-reports) WRITE_REPORTS=1; shift ;;
    --json) JSON_OUT=1; shift ;;
    --repo) ROOT="$2"; shift 2 ;;
    --skills-root) SKILLS_ROOT="$2"; shift 2 ;;
    --hooks-root) HOOKS_ROOT="$2"; shift 2 ;;
    --settings) SETTINGS_JSON="$2"; shift 2 ;;
    --date) DATE="$2"; shift 2 ;;
    -h|--help) usage; exit 0 ;;
    *) echo "unknown arg: $1" >&2; usage >&2; exit 2 ;;
  esac
done

SKILL_AUDIT_DIR="$ROOT/.flywheel/audits/skill-scoping-${DATE}"
HOOKS_AUDIT_DIR="$ROOT/.flywheel/audits/hooks-inventory-${DATE}"
SCORECARD_DIR="$ROOT/.flywheel/audits/claude-code-best-practices-${DATE}"
SKILL_REPORT="$SKILL_AUDIT_DIR/REPORT.md"
HOOKS_REPORT="$HOOKS_AUDIT_DIR/HOOKS.md"
SCORECARD="$SCORECARD_DIR/SCORECARD.md"

json_escape() {
  jq -Rsa . <<<"${1:-}"
}

pass_count=0
fail_count=0
checks_json=""

record_check() {
  local name="$1" status="$2" detail="$3"
  if [[ "$status" == "PASS" ]]; then
    pass_count=$((pass_count + 1))
  else
    fail_count=$((fail_count + 1))
  fi
  local row
  row=$(jq -nc --arg name "$name" --arg status "$status" --arg detail "$detail" \
    '{name:$name,status:$status,detail:$detail}')
  checks_json="${checks_json}${row}"$'\n'
}

first_comment() {
  local file="$1"
  [[ -n "$file" && -f "$file" ]] || return 0
  awk '
    /^#!/ { next }
    /^#/ {
      sub(/^#[[:space:]]*/, "")
      if (length($0) > 0) { print; exit }
    }
  ' "$file" 2>/dev/null || true
}

file_mtime() {
  local file="$1"
  if [[ -z "$file" || ! -f "$file" ]]; then
    printf 'n/a'
  elif stat -f '%Sm' -t '%Y-%m-%d' "$file" >/dev/null 2>&1; then
    stat -f '%Sm' -t '%Y-%m-%d' "$file"
  else
    stat -c '%y' "$file" | cut -d' ' -f1
  fi
}

repo_dirs() {
  find "$ROOT" -maxdepth 1 -mindepth 1 -type d -exec basename {} \; | sort
}

skill_files() {
  [[ -d "$SKILLS_ROOT" ]] || return 0
  find "$SKILLS_ROOT" -name SKILL.md -type f | sort
}

has_description() {
  awk '
    BEGIN { in_fm=0; seen=0 }
    /^---[[:space:]]*$/ { if (in_fm) exit; in_fm=1; next }
    in_fm && /^description[[:space:]]*:/ { seen=1; exit }
    END { exit seen ? 0 : 1 }
  ' "$1"
}

has_trigger_keywords() {
  grep -Eiq '(trigger|keywords|when to use|use when|must use|automatically|invoke|activation)' "$1"
}

has_path_scope() {
  grep -Eiq '(applies_to|path-scop|file pattern|directories|directory|paths?:|scope.*path|only.*repo|only.*project)' "$1"
}

generate_skill_report() {
  mkdir -p "$SKILL_AUDIT_DIR"
  local tmp rows total desc triggers path_scope broad
  tmp="$(mktemp "${TMPDIR:-/tmp}/skill-scope.XXXXXX")"
  rows="$(mktemp "${TMPDIR:-/tmp}/skill-scope-rows.XXXXXX")"
  total=0; desc=0; triggers=0; path_scope=0; broad=0
  : >"$rows"
  while IFS= read -r file; do
    [[ -n "$file" ]] || continue
    total=$((total + 1))
    local rel name d t p flags
    rel="${file#$SKILLS_ROOT/}"
    name="${rel%/SKILL.md}"
    d=0; t=0; p=0; flags=""
    if has_description "$file"; then d=1; desc=$((desc + 1)); else flags="${flags}missing-description "; fi
    if has_trigger_keywords "$file"; then t=1; triggers=$((triggers + 1)); else flags="${flags}missing-trigger "; fi
    if has_path_scope "$file"; then p=1; path_scope=$((path_scope + 1)); else flags="${flags}missing-path-scope "; fi
    if [[ "$t" -eq 0 || "$p" -eq 0 ]]; then broad=$((broad + 1)); fi
    printf '%s\t%s\t%s\t%s\t%s\n' "$name" "$d" "$t" "$p" "${flags:-ok}" >>"$rows"
  done < <(skill_files)

  {
    printf '# Skill Scoping Audit - %s\n\n' "$DATE"
    printf 'Generated by: `.flywheel/scripts/claude-code-best-practices-audit.sh --write-reports`\n\n'
    printf 'Source: `%s`\n\n' "$SKILLS_ROOT"
    printf '## Metrics\n\n'
    printf '| Metric | Count |\n|---|---:|\n'
    printf '| SKILL.md files | %d |\n' "$total"
    printf '| Description field present | %d |\n' "$desc"
    printf '| Explicit trigger keywords | %d |\n' "$triggers"
    printf '| Path scoping via applies_to or equivalent | %d |\n' "$path_scope"
    printf '| Potentially broad activation | %d |\n\n' "$broad"
    printf '## Potentially Broad Skills\n\n'
    printf '| Skill | Description | Trigger | Path scope | Flags |\n|---|---:|---:|---:|---|\n'
    awk -F '\t' '$5 != "ok" { printf "| `%s` | %s | %s | %s | %s |\n", $1, $2, $3, $4, $5; count++; if (count >= 40) exit }' "$rows"
    if ! awk -F '\t' '$5 != "ok" { found=1 } END { exit found ? 0 : 1 }' "$rows"; then
      printf '| none | 1 | 1 | 1 | ok |\n'
    fi
    printf '\n## Notes\n\n'
    printf -- '- Counts are computed from live `SKILL.md` files, not estimates.\n'
    printf -- '- Trigger detection accepts explicit trigger/keyword/use-when language.\n'
    printf -- '- Path scoping accepts `applies_to` or equivalent path/directory scoping language.\n'
  } >"$SKILL_REPORT"
  rm -f "$tmp" "$rows"
}

settings_hooks_tsv() {
  [[ -f "$SETTINGS_JSON" ]] || return 0
  jq -r '
    .hooks as $hooks
    | ["PreToolUse","PostToolUse","Stop"][] as $event
    | ($hooks[$event] // [])[]
    | .matcher as $matcher
    | (.hooks // [])[]
    | [
        $event,
        (if (($matcher // "") == "") then "n/a" else $matcher end),
        (.type // ""),
        (.command // "")
      ] | @tsv
  ' "$SETTINGS_JSON"
}

event_dir_hooks_tsv() {
  local event dir file
  for event in PreToolUse PostToolUse Stop; do
    dir="$HOOKS_ROOT/$event"
    [[ -d "$dir" ]] || continue
    find "$dir" -maxdepth 1 -type f | sort | while IFS= read -r file; do
      printf '%s\t%s\tfile\t%s\n' "$event" "event-dir" "$file"
    done
  done
}

generate_hooks_report() {
  mkdir -p "$HOOKS_AUDIT_DIR"
  local rows candidates settings_count event_dir_count
  rows="$(mktemp "${TMPDIR:-/tmp}/hooks.XXXXXX")"
  candidates="$(mktemp "${TMPDIR:-/tmp}/hooks-candidates.XXXXXX")"
  : >"$rows"; : >"$candidates"
  settings_hooks_tsv >>"$rows"
  event_dir_hooks_tsv >>"$rows"
  settings_count=$(settings_hooks_tsv | wc -l | tr -d ' ')
  event_dir_count=$(event_dir_hooks_tsv | wc -l | tr -d ' ')

  {
    printf '# Hooks Inventory - %s\n\n' "$DATE"
    printf 'Generated by: `.flywheel/scripts/claude-code-best-practices-audit.sh --write-reports`\n\n'
    printf 'Sources: `%s`, `%s`\n\n' "$HOOKS_ROOT" "$SETTINGS_JSON"
    printf '## Metrics\n\n'
    printf '| Metric | Count |\n|---|---:|\n'
    printf '| settings.json PreToolUse/PostToolUse/Stop hooks | %s |\n' "$settings_count"
    printf '| event-directory hook files | %s |\n\n' "$event_dir_count"
    printf '## Hook Rows\n\n'
    printf '| Event | Matcher | Command | Authored date | Purpose | Model-version dependency |\n|---|---|---|---|---|---|\n'
    while IFS=$'\t' read -r event matcher type command; do
      [[ -n "$event" ]] || continue
      local path date purpose model_dep
      path="$command"
      [[ "$path" == /* ]] || path=""
      date="$(file_mtime "$path")"
      purpose="$(first_comment "$path")"
      [[ -n "$purpose" ]] || purpose="$type hook"
      model_dep="none found"
      if [[ -n "$path" && -f "$path" ]] && grep -Eiq '(model|opus|sonnet|haiku|claude-|gpt-)' "$path"; then
        model_dep="mentions model/runtime terms"
      fi
      printf '| %s | `%s` | `%s` | %s | %s | %s |\n' "$event" "${matcher:-n/a}" "$command" "$date" "$purpose" "$model_dep"
      if [[ "$command" == *handshakes-never-gate-on-joshua* ]]; then
        printf '%s\t%s\t%s\n' "$event" "$command" "thin wrapper into repo script; review after L145/orchestrator substrate is stable" >>"$candidates"
      elif [[ "$command" == *.bak* || "$command" == *archive* || "$command" == *deprecated* ]]; then
        printf '%s\t%s\t%s\n' "$event" "$command" "backup/archive/deprecated hook path" >>"$candidates"
      fi
    done <"$rows"
    printf '\n## Retirement Candidates\n\n'
    if [[ -s "$candidates" ]]; then
      printf '| Event | Command | Rationale |\n|---|---|---|\n'
      awk -F '\t' '{ printf "| %s | `%s` | %s |\n", $1, $2, $3 }' "$candidates"
    else
      printf 'No retirement candidates found. Existing configured hooks are active safety or capture gates with no stale model-version dependency detected.\n'
    fi
  } >"$HOOKS_REPORT"
  rm -f "$rows" "$candidates"
}

check_claudeignore() {
  local file="$ROOT/.claudeignore" missing=()
  local patterns=(
    ".flywheel/jeff-corpus/qdrant-data/"
    ".flywheel/runtime/"
    ".flywheel/state.db*"
    ".flywheel/logs/"
    ".flywheel/**/inventory*.jsonl"
    "node_modules/"
    "__pycache__/"
    ".repo_janitor_workspace/"
    ".flywheel/audits/**/*.jsonl"
  )
  if [[ ! -f "$file" ]]; then
    record_check ".claudeignore" "FAIL" "missing"
    return
  fi
  local p
  for p in "${patterns[@]}"; do
    grep -Fxq "$p" "$file" || missing+=("$p")
  done
  if [[ "${#missing[@]}" -eq 0 ]]; then
    record_check ".claudeignore" "PASS" "required exclusions present"
  else
    record_check ".claudeignore" "FAIL" "missing: ${missing[*]}"
  fi
}

check_repo_map() {
  local file="$ROOT/REPO-MAP.md" missing=()
  if [[ ! -f "$file" ]]; then
    record_check "REPO-MAP.md" "FAIL" "missing"
    return
  fi
  while IFS= read -r dir; do
    grep -Fq "\`$dir\`" "$file" || missing+=("$dir")
  done < <(repo_dirs)
  if [[ "${#missing[@]}" -eq 0 ]]; then
    record_check "REPO-MAP.md" "PASS" "all top-level directories named"
  else
    record_check "REPO-MAP.md" "FAIL" "missing dirs: ${missing[*]}"
  fi
}

check_subdir_claude() {
  local paths=(
    ".flywheel/scripts/CLAUDE.md"
    ".flywheel/doctrine/CLAUDE.md"
    ".flywheel/audits/CLAUDE.md"
    "tests/CLAUDE.md"
    "bin/CLAUDE.md"
  )
  local missing=()
  local p
  for p in "${paths[@]}"; do
    if [[ ! -f "$ROOT/$p" ]] || ! grep -q '^## Conventions' "$ROOT/$p" || ! awk '/^## Conventions/{seen=1;next} seen && /^- /{found=1} END{exit found?0:1}' "$ROOT/$p"; then
      missing+=("$p")
    fi
  done
  if [[ "${#missing[@]}" -eq 0 ]]; then
    record_check "subdirectory CLAUDE.md" "PASS" "five conventions files present"
  else
    record_check "subdirectory CLAUDE.md" "FAIL" "missing conventions: ${missing[*]}"
  fi
}

check_skill_report() {
  if [[ ! -f "$SKILL_REPORT" ]]; then
    record_check "skill scoping report" "FAIL" "missing"
    return
  fi
  local total desc triggers path_scope
  total=$(awk -F '|' '/SKILL.md files/ { gsub(/ /, "", $3); print $3 }' "$SKILL_REPORT")
  desc=$(awk -F '|' '/Description field present/ { gsub(/ /, "", $3); print $3 }' "$SKILL_REPORT")
  triggers=$(awk -F '|' '/Explicit trigger keywords/ { gsub(/ /, "", $3); print $3 }' "$SKILL_REPORT")
  path_scope=$(awk -F '|' '/Path scoping/ { gsub(/ /, "", $3); print $3 }' "$SKILL_REPORT")
  if [[ "$total" =~ ^[0-9]+$ && "$desc" =~ ^[0-9]+$ && "$triggers" =~ ^[0-9]+$ && "$path_scope" =~ ^[0-9]+$ && "$total" -gt 0 ]]; then
    record_check "skill scoping report" "PASS" "real counts total=$total description=$desc triggers=$triggers path_scope=$path_scope"
  else
    record_check "skill scoping report" "FAIL" "missing numeric metrics"
  fi
}

check_hooks_report() {
  if [[ ! -f "$HOOKS_REPORT" ]]; then
    record_check "hooks inventory" "FAIL" "missing"
    return
  fi
  if grep -q '^## Retirement Candidates' "$HOOKS_REPORT" && { grep -q '^| Event | Command | Rationale |' "$HOOKS_REPORT" || grep -q '^No retirement candidates found' "$HOOKS_REPORT"; }; then
    record_check "hooks inventory" "PASS" "inventory includes retirement disposition"
  else
    record_check "hooks inventory" "FAIL" "missing retirement disposition"
  fi
}

write_scorecard() {
  mkdir -p "$SCORECARD_DIR"
  {
    printf '# Claude Code Best Practices Scorecard - %s\n\n' "$DATE"
    printf 'Generated by: `.flywheel/scripts/claude-code-best-practices-audit.sh --write-reports`\n\n'
    printf 'Source: https://claude.com/blog/how-claude-code-works-in-large-codebases-best-practices-and-where-to-start\n\n'
    printf '## Summary\n\n'
    printf '| Metric | Value |\n|---|---:|\n'
    printf '| Status | %s |\n' "$([[ "$fail_count" -eq 0 ]] && echo PASS || echo FAIL)"
    printf '| Score | %s |\n' "$score"
    printf '| Passed checks | %d |\n' "$pass_count"
    printf '| Failed checks | %d |\n\n' "$fail_count"
    printf '## Checks\n\n'
    printf '| Check | Status | Detail |\n|---|---|---|\n'
    printf '%s' "$checks_json" | jq -r '"| " + .name + " | " + .status + " | " + .detail + " |"'
    printf '\n## Reports\n\n'
    printf -- '- Skill scoping: `%s`\n' "${SKILL_REPORT#$ROOT/}"
    printf -- '- Hooks inventory: `%s`\n' "${HOOKS_REPORT#$ROOT/}"
  } >"$SCORECARD"
}

if [[ "$WRITE_REPORTS" -eq 1 ]]; then
  generate_skill_report
  generate_hooks_report
fi

check_claudeignore
check_repo_map
check_subdir_claude
check_skill_report
check_hooks_report

total_checks=$((pass_count + fail_count))
score="0"
if [[ "$total_checks" -gt 0 ]]; then
  score=$(awk -v p="$pass_count" -v t="$total_checks" 'BEGIN { printf "%.3f", p/t }')
fi

if [[ "$WRITE_REPORTS" -eq 1 ]]; then
  write_scorecard
fi

if [[ "$JSON_OUT" -eq 1 ]]; then
  checks_array="$(printf '%s' "$checks_json" | jq -s '.')"
  jq -nc \
    --arg schema_version "claude-code-best-practices-audit/v1" \
    --arg status "$([[ "$fail_count" -eq 0 ]] && echo PASS || echo FAIL)" \
    --argjson pass "$pass_count" \
    --argjson fail "$fail_count" \
    --argjson score "$score" \
    --arg scorecard "$SCORECARD" \
    --argjson checks "$checks_array" \
    '{schema_version:$schema_version,status:$status,score:$score,pass:$pass,fail:$fail,scorecard:$scorecard,checks:$checks}'
else
  printf 'claude-code-best-practices-audit status=%s score=%s pass=%d fail=%d\n' \
    "$([[ "$fail_count" -eq 0 ]] && echo PASS || echo FAIL)" "$score" "$pass_count" "$fail_count"
  printf '%s' "$checks_json" | jq -r '"- " + .status + " " + .name + ": " + .detail'
fi

[[ "$fail_count" -eq 0 ]]
